int	ft_iterative_factorial(int nb)
{
	int	num;
	int	i;

	num = 1;
	i = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (i < nb + 1)
	{
		num = num * i;
		i++;
	}
	return (num);
}
