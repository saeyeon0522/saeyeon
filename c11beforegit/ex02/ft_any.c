int	ft_any(char **tab, int (*f) (char*))
{
	int	i;

	i = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			if (f(tab[i][j]) != 0)
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
