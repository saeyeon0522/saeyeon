int	ft_atoi(char *str)
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	while (*str)
	{
		while ((*str >= 9 && *str <= 13) || *str == ' ')
			str++;
		while (*str == '-' || *str == '+')
		{
			if (*str == '-')
				i *= -1;
			str++;
		}
		while (*str >= '0' && *str <= '9')
		{
			n *= 10;
			n += *str - '0';
			str++;
		}
		break ;
	}
	return (n * i);
}
